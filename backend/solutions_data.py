def get_solutions_by_names(solution_names: List[str]) -> List[dict]:
    """Get solutions by their names"""
    solutions_dict = {sol['name']: sol for sol in AI_SOLUTIONS_DATASET}
    return [solutions_dict[name] for name in solution_names if name in solutions_dict]

def search_solutions(query: str, limit: int = 20) -> List[dict]:
    """Search solutions by query"""
    query_lower = query.lower()
    matching_solutions = []
    
    for solution in AI_SOLUTIONS_DATASET:
        if (query_lower in solution['name'].lower() or 
            query_lower in solution['description'].lower() or
            query_lower in solution['category'].lower() or
            any(query_lower in use_case.lower() for use_case in solution['use_cases'])):
            matching_solutions.append(solution)
            
    return matching_solutions[:limit]